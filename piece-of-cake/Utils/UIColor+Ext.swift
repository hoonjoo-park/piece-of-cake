import UIKit

extension UIColor {
    static var gradientDarkGrey: UIColor {
        return UIColor(red: 239 / 255, green: 241 / 255, blue: 241 / 255, alpha: 1)
    }

    static var gradientLightGrey: UIColor {
        return UIColor(red: 201 / 255, green: 201 / 255, blue: 201 / 255, alpha: 1)
    }
}
